var obsidian = __toESM(require("obsidian"));
function createIconButton(parent, config) {
  const btn = parent.createEl("button", {
    cls: `${CSS_CLASSES.iconBtn} ${config.cls || ""}`
  });
  if (typeof obsidian.setIcon === "function") {
    obsidian.setIcon(btn, config.icon);
  } else {
    btn.setText(config.icon);
  }
  btn.setAttr("title", config.title);
  btn.addEventListener("click", (e) => {
    e.stopPropagation();
    config.onClick(e);
  });
  return btn;
}
function createPlaybackToggle(parent, onToggle, registry) {
  const btn = parent.createEl("button", {
    cls: `qr-main-toggle ${CSS_CLASSES.btn}`
  });
  registry.register("playbackToggle", btn);
  if (typeof obsidian.setIcon === "function") {
    obsidian.setIcon(btn, "play");
  } else {
    btn.setText("PLAY");
  }
  btn.addEventListener("click", () => onToggle());
  return btn;
}
function updatePlaybackUI(registry, isPlaying) {
  const btn = registry.get("playbackToggle");
  if (!btn)
    return;
  const icon = isPlaying ? "pause" : "play";
  if (typeof obsidian.setIcon === "function") {
    obsidian.setIcon(btn, icon);
  } else {
    btn.setText(icon.toUpperCase());
  }
}

