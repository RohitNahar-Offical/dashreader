var QuickReaderPlugin = class extends import_obsidian3.Plugin {
  async onload() {
    await this.loadSettings();
    this.addRibbonIcon("zap", "Open Quick Reader", () => {
      this.openReader();
    });
    this.addCommand({
      id: "open-reader-note",
      name: "Read entire note",
      callback: () => {
        const view = this.app.workspace.getActiveViewOfType(import_obsidian3.MarkdownView);
        if (view) {
          this.openReader(view.editor.getValue());
        } else {
          new import_obsidian3.Notice("Please open a note first");
        }
      }
    });
    this.addCommand({
      id: "open-reader-selection",
      name: "Read selection",
      editorCallback: (editor) => {
        const selection = editor.getSelection();
        if (selection) {
          this.openReader(selection);
        } else {
          new import_obsidian3.Notice("Please select some text first");
        }
      }
    });
    this.registerEvent(
      this.app.workspace.on("editor-menu", (menu, editor) => {
        const selection = editor.getSelection();
        if (selection) {
          menu.addItem((item) => {
            item.setTitle("Read with Quick Reader").setIcon("zap").onClick(() => this.openReader(selection));
          });
        }
      })
    );
    this.addSettingTab(new QuickReaderSettingTab(this.app, this));
  }
  onunload() {
  }
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
  }
  /**
   * Helper to open the modal with specific text
   */
  openReader(text) {
    const modal = new QuickReaderModal(this.app, this);
    const activeFile = this.app.workspace.getActiveFile();
    const fileName = activeFile ? activeFile.basename : "Selection";
    if (text) {
      modal.openWithText(text, fileName);
    } else {
      const view = this.app.workspace.getActiveViewOfType(import_obsidian3.MarkdownView);
      if (view) {
        modal.openWithText(view.editor.getValue(), fileName);
      } else {
        new import_obsidian3.Notice("No text to read. Please open a note.");
      }
    }
  }
};
